package Day1;

public class Addition {
    static void main(String[] args) {
        int num1 = 20;
        int num2 = 30;
        int sum = num1 + num2;
        System.out.println("Sum is: " + sum);
        System.out.println("Sum of num1 and num2 is: " + sum);
        System.out.println("Sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
