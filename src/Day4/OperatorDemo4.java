package Day4;

public class OperatorDemo4 {
    static void main(String[] args) {
        //4.Relational Operator > < >= <= == !=
        int a = 10;
        int b = 20;
        System.out.println(a > b);//false
        System.out.println(a < b);//true
        System.out.println(a >= b);//false
        System.out.println(a <= b);//true
        System.out.println(a == b);//false
        System.out.println(a != b);//true

    }
}
